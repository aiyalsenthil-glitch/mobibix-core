// Convert rupees to paisa
function toPaisa(rupees) {
  return Math.round(rupees * 100);
}

async function seed() {
  console.log('🌱 Seeding market price intelligence...');
  let inserted = 0;
  let skipped = 0;

  for (const entry of MODELS) {
    for (const storage of entry.storages) {
      const key = `${entry.brand}|${entry.model}|${storage}`;
      const prices = PRICE_TABLE[key];

      if (!prices) {
        // Use brand-model average estimate based on storage tier
        // Skip models without explicit price data rather than guessing
        skipped++;
        continue;
      }

      // Apply 50% reduction — trade-in buyback prices, not resale prices
      const [avgMarket, avgOffer] = prices.map(v => Math.round(v * 0.5));

      try {
        await prisma.$executeRaw`
          INSERT INTO mb_market_price_intel
            (id, brand, model, storage, "avgMarketValue", "minMarketValue", "maxMarketValue", "avgOffer", "sampleCount", "dataSource", "lastUpdated")
          VALUES
            (gen_random_uuid()::text, ${entry.brand}, ${entry.model}, ${storage},
             ${toPaisa(avgMarket)},
             ${toPaisa(Math.round(avgMarket * 0.85))},
             ${toPaisa(Math.round(avgMarket * 1.15))},
             ${toPaisa(avgOffer)},
             1, 'CASHIFY', NOW())
          ON CONFLICT (brand, model, storage)
          DO UPDATE SET
            "avgMarketValue" = EXCLUDED."avgMarketValue",
            "minMarketValue" = EXCLUDED."minMarketValue",
            "maxMarketValue" = EXCLUDED."maxMarketValue",
            "avgOffer"       = EXCLUDED."avgOffer",
            "lastUpdated"    = NOW()
        `;
        inserted++;
        process.stdout.write('.');
      } catch (err) {
        console.error(`\nFailed to insert ${key}:`, err.message);
      }
    }
  }

  console.log(`\n✅ Done. Inserted/Updated: ${inserted}, Skipped (no price data): ${skipped}`);
}

seed()
  .catch((e) => { console.error(e); process.exit(1); })
  .finally(() => prisma.$disconnect());
